package io.jiffy.stax.plugin.pe.action.ini;

public class Option {
    private String name;
    private String value;

    public Option() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
